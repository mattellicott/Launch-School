class PokerHand
  def initialize(deck)
    @deck = deck
    @hand = 5.times.with_object([]) { |_, card| card << @deck.draw }
             .sort_by { |rank, _| rank }
  end

  def print
    @hand.each { |card| puts card }
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def royal_flush?
    @hand.map(&:rank) == [10, 'Jack', 'Queen', 'King', 'Ace'] && flush?
  end

  def straight_flush?
    straight? && flush?
  end

  def four_of_a_kind?
    card_ranks = @hand.map(&:rank)
    card_ranks.any? { |rank| card_ranks.count(rank) == 4 }
  end

  def full_house?
    @hand.map(&:rank).uniq.size == 2
  end

  def flush?
    @hand.map(&:suit).all? { |suit| suit == @hand.first.suit }
  end

  def straight?
    4.times { |n| return false unless @hand[n].value + 1 == @hand[n + 1].value }
  end

  def three_of_a_kind?
    card_ranks = @hand.map(&:rank)
    card_ranks.any? { |rank| card_ranks.count(rank) == 3 }
  end

  def two_pair?
    @hand.map(&:rank).uniq.size == 3
  end

  def pair?
    @hand.map(&:rank).uniq.size == 4
  end
end

class Deck
  RANKS = ((2..10).to_a + %w(Jack Queen King Ace)).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  def initialize
    @deck = RANKS.product(SUITS).map do |rank, suit|
      Card.new(rank, suit)
    end.shuffle
  end

  def draw
    initialize if @deck.empty?
    @deck.pop
  end
end

class Card
  include Comparable
  attr_reader :rank, :suit

  VALUES = { 'Jack' => 11, 'Queen' => 12, 'King' => 13, 'Ace' => 14 }

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def to_s
    "#{rank} of #{suit}"
  end

  def value
    VALUES.fetch(rank, rank)
  end

  def <=>(other_card)
    value <=> other_card.value
  end
end

hand = PokerHand.new(Deck.new)
hand.print
puts hand.evaluate

